def is_need(filepath):
    """
    判断文件是否需要复制。

    Args:
        filepath: 相对于源文件夹的相对路径（使用正斜杠或反斜杠均可）。
                  例如 '化学/001 物质的分类与转化.ppt'

    Returns:
        bool: True 表示需要复制，False 表示不需要复制。
    """
    if not isinstance(filepath, str):
        return False

    # 统一路径分隔符并去除首尾空白
    filepath = filepath.replace("\\", "/").strip()

    # 获取文件名和扩展名
    filename = os.path.basename(filepath)
    dot_pos = filename.rfind(".")
    if dot_pos == -1:
        return False  # 无扩展名，跳过

    ext = filename[dot_pos + 1 :].lower()
    if ext not in _VALID_EXTS:
        return False

    # 检查是否在忽略列表中
    if filepath in _IGNORE_SET:
        return False

    return True


def should_ignore(rel_path):
    """
    判断给定的相对路径是否应该被忽略。
    这是 is_need 的反向接口。
    """
    return not is_need(rel_path)
